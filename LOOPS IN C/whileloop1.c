#include <stdio.h>

void main()
{
    int i = 1;
    while(i <= 10)
    {
        printf("%d\n", i);
        i++;
    }
} 

// write down natural numbers from 1 to 10 in reverse

#include <stdio.h>

void main()
{
    int i = 10;
    while(i >= 1)
    {
        printf("%d\n", i);
        i--;
    }
} 